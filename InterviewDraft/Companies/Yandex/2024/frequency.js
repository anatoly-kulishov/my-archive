/**
 * Описание:
 * Напишите функцию frequency,
 * которая принимает массив целых чисел и возвращает строку,
 * представляющую последовательные числа как диапазоны.
 *
 * Требования:
 * Если числа идут подряд, объединить их в диапазон X-Y.
 * Если числа не подряд, они выводятся отдельно.
 * Результат должен быть отсортирован и представлен как строка чисел и диапазонов, разделённых запятыми.
 * Пустой массив → пустая строка.
 *
 * Примечания:
 * Числа могут быть в любом порядке во входном массиве.
 * Результат должен учитывать только целые числа.
 */
function frequency(list) {
    const sortedList = list.sort((a, b) => a - b); // Сортируем список
    const result = [];

    let start = sortedList[0]; // Начало диапазона
    let end = start; // Конец диапазона

    for (let i = 1; i <= sortedList.length; i++) { // Проходим по списку
        const current = sortedList[i];
        if (current === end + 1) {
            // Если текущий элемент продолжает диапазон
            end = current;
        } else {
            // Если текущий элемент не продолжает диапазон
            result.push(start === end ? `${start}` : `${start}-${end}`);
            start = current;
            end = current;
        }
    }

    return result.join(','); // Объединяем диапазоны в строку
}

console.log(frequency([1, 3, 2, 9, 8, 11, 0])); // '0-3,8-9,11'